package com.sau.ums.bean;

public class Room {

    private String room;

    private String roomtype;

    private float publicarea;

    private float roomarea;

    private float area;

    private float loftarea;

    private String number;

    private String ps;

    public String getRoom() {
        return room;
    }

    public void setRoom(String room) {
        this.room = room;
    }

    public String getRoomtype() {
        return roomtype;
    }

    public void setRoomtype(String roomtype) {
        this.roomtype = roomtype;
    }

    public float getPublicarea() {
        return publicarea;
    }

    public void setPublicarea(float publicarea) {
        this.publicarea = publicarea;
    }

    public float getRoomarea() {
        return roomarea;
    }

    public void setRoomarea(float roomarea) {
        this.roomarea = roomarea;
    }

    public float getArea() {
        return area;
    }

    public void setArea(float area) {
        this.area = area;
    }

    public float getLoftarea() {
        return loftarea;
    }

    public void setLoftarea(float loftarea) {
        this.loftarea = loftarea;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getPs() {
        return ps;
    }

    public void setPs(String ps) {
        this.ps = ps;
    }

}
